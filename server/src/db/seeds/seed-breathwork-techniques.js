const BATCH_SIZE = 10;
const COLUMNS = [
  'name', 'sanskrit_name', 'tradition', 'category', 'purposes', 'difficulty',
  'safety_level', 'protocol', 'description', 'instructions', 'benefits',
  'contraindications', 'caution_note', 'source',
  'beginner_duration_min', 'beginner_duration_max',
  'intermediate_duration_min', 'intermediate_duration_max',
  'advanced_duration_min', 'advanced_duration_max',
  'pre_workout_compatible', 'post_workout_compatible', 'standalone_compatible',
];

async function seed() {
  console.log(`Seeding ${techniques.length} breathwork techniques...`);

  const client = await pool.connect();
  try {
    await client.query('BEGIN');

    // Clear existing data
    await client.query('TRUNCATE breathwork_techniques RESTART IDENTITY CASCADE');
    console.log('Cleared existing breathwork techniques.');

    let inserted = 0;

    for (let i = 0; i < techniques.length; i += BATCH_SIZE) {
      const batch = techniques.slice(i, i + BATCH_SIZE);
      const values = [];
      const params = [];
      let paramIndex = 1;

      for (const t of batch) {
        const tags = TAGS_BY_NAME[t.name];
        if (!tags) throw new Error(`Missing S11-T2 tags for technique: ${t.name}`);

        const placeholders = COLUMNS.map((col) => {
          const idx = paramIndex++;
          if (col === 'protocol') return `$${idx}::jsonb`;
          return `$${idx}`;
        });
        values.push(`(${placeholders.join(', ')})`);

        params.push(
          t.name,
          t.sanskrit_name,
          t.tradition,
          t.category,
          t.purposes,
          t.difficulty,
          t.safety_level,
          JSON.stringify(t.protocol),
          t.description,
          t.instructions,
          t.benefits,
          t.contraindications,
          t.caution_note,
          t.source,
          ...tags,
        );
      }

      const result = await client.query(
        `INSERT INTO breathwork_techniques (${COLUMNS.join(', ')})
         VALUES ${values.join(', ')}`,
        params,
      );

      inserted += result.rowCount;
    }

    await client.query('COMMIT');
    console.log(`\nInserted ${inserted} techniques.`);
  } catch (err) {
    await client.query('ROLLBACK');
    throw err;
  } finally {
    client.release();
  }

  // Verify counts (outside transaction, using pool)
  const total = await pool.query('SELECT COUNT(*)::int AS total FROM breathwork_techniques');
  console.log(`Total in table: ${total.rows[0].total}`);

  const byTradition = await pool.query(
    `SELECT tradition, COUNT(*)::int AS count FROM breathwork_techniques GROUP BY tradition ORDER BY count DESC`,
  );
  console.log('\nBy tradition:');
  for (const r of byTradition.rows) console.log(`  ${r.tradition}: ${r.count}`);

  const bySafety = await pool.query(
    `SELECT safety_level, COUNT(*)::int AS count FROM breathwork_techniques GROUP BY safety_level ORDER BY count DESC`,
  );
  console.log('\nBy safety level:');
  for (const r of bySafety.rows) console.log(`  ${r.safety_level}: ${r.count}`);

  const redTechniques = await pool.query(
    `SELECT name, caution_note FROM breathwork_techniques WHERE safety_level = 'red' ORDER BY name`,
  );
  console.log('\nRed-level techniques (require caution):');
  for (const r of redTechniques.rows) console.log(`  ${r.name}: ${r.caution_note?.substring(0, 60)}...`);
}

seed()
  .catch((err) => {
    console.error('Seed failed:', err);
    process.exitCode = 1;
  })
  .finally(() => pool.end());
